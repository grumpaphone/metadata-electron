let mainWindow: BrowserWindow;
let fileWatcher: chokidar.FSWatcher | null = null;
let fileWatcherPath: string | null = null;
let fileWatcherLastEvent: Date | null = null;
let fileWatcherError: string | null = null;

// Path traversal protection: allowlisted roots populated by user-driven
// dialog selections and directory scans.
const allowedRoots: Set<string> = new Set();

// In-flight mirror operations that have been asked to cancel. The
// `mirrorFiles` handler passes an `isCancelled` closure to the service that
// checks this set; it is cleared in the handler's finally block.
const cancelledOps: Set<string> = new Set();

const MAX_AUDIO_FILE_BYTES = 2 * 1024 * 1024 * 1024; // 2GB

const addAllowedRoot = (rootPath: string): void => {
	if (!rootPath) return;
	allowedRoots.add(path.resolve(rootPath));
};

const assertPathUnderAllowedRoot = (p: string): void => {
	if (!p || typeof p !== 'string') {
		throw new Error('Path not under an allowed root: ' + p);
	}
	const resolved = path.resolve(p);
	for (const root of allowedRoots) {
		if (resolved === root || resolved.startsWith(root + path.sep)) {
			return;
		}
	}
	throw new Error('Path not under an allowed root: ' + p);
};

const collectWavFilesRecursively = async (
	dirPath: string,
	visited: Set<string> = new Set()
): Promise<string[]> => {
	const entries = await fs.promises.readdir(dirPath, { withFileTypes: true });
	const wavFiles: string[] = [];

	for (const entry of entries) {
		const entryPath = path.join(dirPath, entry.name);
		if (entry.isDirectory()) {
			try {
				const realSubdir = await fs.promises.realpath(entryPath);
				if (visited.has(realSubdir)) {
					continue;
				}
				visited.add(realSubdir);
				wavFiles.push(
					...(await collectWavFilesRecursively(entryPath, visited))
				);
			} catch (error) {
				log.warn(
					`Skipping subdirectory ${entryPath} due to error:`,
					error
				);
			}
			continue;
		}

		if (entry.isFile() && path.extname(entry.name).toLowerCase() === '.wav') {
			wavFiles.push(entryPath);
		}
	}

	return wavFiles.sort((left, right) => left.localeCompare(right));
};

const createWindow = () => {
	mainWindow = new BrowserWindow({
		height: 800,
		width: 1200,
		minHeight: 600,
		minWidth: 1000,
		frame: false, // custom chrome
		transparent: false, // fully opaque window
		hasShadow: true,
		backgroundColor: '#0b1420',
		titleBarStyle: 'hidden',
		roundedCorners: true,
		webPreferences: {
			preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
			nodeIntegration: false,
			contextIsolation: true,
			webSecurity: true,
		},
		acceptFirstMouse: true,
	});

	// Ensure native traffic lights are hidden when using custom chrome
	try {
		// macOS only
		(mainWindow as any).setWindowButtonVisibility?.(false);
	} catch (e) {
		// no-op
	}

	// Log preload errors for debugging
	mainWindow.webContents.on('preload-error', (event, preloadPath, error) => {
		log.error('Preload script error:', preloadPath, error);
	});

	mainWindow.loadURL(MAIN_WINDOW_WEBPACK_ENTRY);

	if (!app.isPackaged) {
		mainWindow.webContents.openDevTools();
	}
};

const getAgentStatusSnapshot = (): AgentStatus[] => {
	const watcherActive = Boolean(fileWatcher);
	const watcherStatus = (() => {
		if (fileWatcherError) {
			return `Error: ${fileWatcherError}`;
		}
		if (watcherActive && fileWatcherPath) {
			return `Watching ${fileWatcherPath}`;
		}
		return 'Idle';
	})();

	const statuses: AgentStatus[] = [
		{
			name: 'file-watcher',
			active: watcherActive,
			lastRun: fileWatcherLastEvent
				? fileWatcherLastEvent.toISOString()
				: undefined,
			status: watcherStatus,
			error: fileWatcherError ?? undefined,
		},
		{
			name: 'auto-save',
			active: false,
			status: 'Manual save only',
		},
	];

	return statuses;
};

const broadcastAgentStatuses = () => {
	if (!mainWindow || mainWindow.isDestroyed()) {
		return;
	}
	mainWindow.webContents.send(
		CHANNELS.onAgentStatusChange,
		getAgentStatusSnapshot()
	);
};

const startFileWatcher = async (targetPath: string | null): Promise<void> => {
	if (fileWatcher) {
		await fileWatcher.close();
		fileWatcher = null;
	}

	if (!targetPath) {
		fileWatcherPath = null;
		fileWatcherLastEvent = null;
		fileWatcherError = null;
		broadcastAgentStatuses();
		return;
	}

	fileWatcher = chokidar.watch(targetPath, {
		persistent: true,
		ignoreInitial: true,
	});

	fileWatcherPath = targetPath;
	fileWatcherLastEvent = null;
	fileWatcherError = null;
	broadcastAgentStatuses();

	fileWatcher.on('change', (changedPath: string) => {
		fileWatcherLastEvent = new Date();
		if (mainWindow && !mainWindow.isDestroyed()) {
			mainWindow.webContents.send(CHANNELS.onFileChanged, changedPath);
		}
		broadcastAgentStatuses();
	});

	fileWatcher.on('error', (error) => {
		log.error(`File watcher error: ${error}`);
		fileWatcherError = error instanceof Error ? error.message : String(error);
		broadcastAgentStatuses();
	});

	fileWatcher.on('ready', () => {
		broadcastAgentStatuses();
	});
};

app.on('ready', () => {
	initAutoUpdater();
	createWindow();
});

app.on('window-all-closed', () => {
	if (process.platform !== 'darwin') {
		app.quit();
	}
});

app.on('activate', () => {
	if (BrowserWindow.getAllWindows().length === 0) {
		createWindow();
	}
});

