// Start the crash reporter as early as possible — before `app` is ready —
// so native crashes during startup are captured. When `CRASH_REPORT_URL` is
// unset we pass an empty submit URL and set `uploadToServer: false`, which
// keeps the reporter wired in (local minidumps still written) without trying
// to POST anywhere.
crashReporter.start({
	productName: 'Metadata Editor',
	companyName: 'metadata-electron',
	submitURL: process.env.CRASH_REPORT_URL ?? '',
	uploadToServer: !!process.env.CRASH_REPORT_URL,
	ignoreSystemCrashHandler: false,
});

